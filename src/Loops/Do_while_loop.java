package Loops;

public class Do_while_loop {

	public static void main(String[] args)
	{
// i want print my name 3 times
		int i=1;
		
		do
		{
			System.out.println("incremental order");
			i++;
		}
		while(i<=3);
		
		// i want to print 50-1 numbers
		int j=50;
		do
		{
			System.out.println(j);
			j--;
		}
		while(j>=1);
		
	}

}
